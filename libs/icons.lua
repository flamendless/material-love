local function get (...)
    local s = ""

    for _,v in ipairs{...} do
        if type(v) == "table" then
            s = s .. get(unpack(v))
        else
            if not icons[v] then error('The icon "'..v..'" is not a valid icon',2) end
            s = s .. icons[v]
        end
    end

    return s
end

local icon = {}

icon.get = get

icon.list = function ()
    local t = {}

    for k,v in pairs (icons) do
        t[k] = v
    end

    return t
end

icon.names = function ()
    local t = {}

    for k,_ in pairs (icons) do
        t[#t + 1] = k
    end

    return t
end

return icon
